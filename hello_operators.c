#include <stdio.h>

int main(void)
{
    int this_is_a_number;

    this_is_a_number = 6 + 2;
    printf("6 + 2 = %d\n", this_is_a_number);

    this_is_a_number = 6 - 2;
    printf("6 - 2 = %d\n", this_is_a_number);

    this_is_a_number = 6 * 2;
    printf("6 * 2 = %d\n", this_is_a_number);

    this_is_a_number = 6 / 2;
    printf("6 / 2 = %d\n", this_is_a_number);

    return 0;
}
