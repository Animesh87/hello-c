
// Array is considered a data structure for which size is fixed at the
// compile time and array memory is allocated either from Data section
// (e.g. global array) or Stack section (e.g. local array).

// Linked List is considered a data structure for which size is not
// fixed and memory is allocated from Heap section (e.g. using malloc()
//  etc.) as and when needed. In this sense, array is taken as a static
// data structure (residing in Data or Stack section) while linked list
// is taken as a dynamic data structure (residing in Heap section).


